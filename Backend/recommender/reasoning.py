_MUSIC_BASE: dict[str, str] = {
    "happy":    "احساس شادی تشخیص داده شد — موسیقی پرانرژی و مثبت انتخاب شد.",
    "sad":      "به دلیل غم، موسیقی آرام و احساسی برای همراهی انتخاب شد.",
    "stressed": "سطح استرس بالاست — موسیقی آرام‌بخش برای کاهش تنش انتخاب شد.",
    "tired":    "خستگی تشخیص داده شد — موسیقی ملایم و آرامش‌بخش مناسب است.",
    "excited":  "هیجان بالا تشخیص داده شد — موسیقی پرانرژی متناسب است.",
    "calm":     "حالت آرام تشخیص داده شد — موسیقی کلاسیک یا محیطی پیشنهاد می‌شود.",
    "angry":    "به دلیل تشخیص ناراحتی، موسیقی آرام‌بخش و مثبت انتخاب شد.",
    "romantic": "حس عاشقانه تشخیص داده شد — موسیقی رمانتیک مناسب است.",
    "focused":  "حالت تمرکز تشخیص داده شد — موسیقی بی‌کلام برای تمرکز بهتر.",
    "neutral":  "پیشنهادهای متنوع موسیقی برای حالت عمومی انتخاب شدند.",
}


# ── Public API ─────────────────────────────────────────────────────────────────

def movie_reason(mood: str, time_period: str, safety: bool) -> str:
    base    = _MOVIE_BASE.get(mood, _MOVIE_BASE["neutral"])
    suffix  = _TIME_SUFFIX.get(time_period, "")
    safety_ = _SAFETY_SUFFIX if safety else ""
    return f"{base}{suffix}{safety_}"


def music_reason(mood: str, activity: str, safety: bool) -> str:
    base     = _MUSIC_BASE.get(mood, _MUSIC_BASE["neutral"])
    activity_ = (
        f" فعالیت تشخیص داده‌شده ({activity}) این انتخاب را تأیید می‌کند."
        if activity != "general" else ""
    )
    safety_ = " محتوای آرام‌تر به دلیل وضعیت احساسی انتخاب شد." if safety else ""
    return f"{base}{activity_}{safety_}"